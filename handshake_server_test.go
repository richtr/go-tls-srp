func testServerScript(t *testing.T, name string, serverScript [][]byte, config *Config, ignore interface{}) {
	c, s := net.Pipe()
	srv := Server(s, config)
	userc := make(chan string, 1)
	go func() {
		srv.Write([]byte("hello, world\n"))
		srv.Close()
		s.Close()
		st := srv.ConnectionState()

		userc <- st.SRPUser
	}()

	for i, b := range serverScript {
		if i%2 == 0 {
			c.Write(b)
			continue
		}
		bb := make([]byte, len(b))
		n, err := io.ReadFull(c, bb)
		if err != nil {
			t.Fatalf("%s #%d: %s\nRead %d, wanted %d, got %x, wanted %x\n", name, i, err, n, len(bb), bb[:n], b)
		}
		if !bytes.Equal(b, bb) {
			t.Fatalf("%s #%d: mismatch on read: got:%x want:%x", name, i, bb, b)
		}
	}
	c.Close()

	user := <-userc
	if user != srpUser {
		t.Fatalf("%s: bad SRPUser, expected %q, saw %q", name, srpUser, user)
	}
}

