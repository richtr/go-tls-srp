type TableLookup map[string]string

var userTab = TableLookup(map[string]string{
	srpUser: srpPassword,
})

func (tl TableLookup) Lookup(user string) (v, s []byte, grp SRPGroup, err error) {
	grp = SRPGroup4096

	p := tl[user]
	if p == "" {
		return nil, nil, grp, nil
	}
	v = SRPVerifier(user, p, srpSalt, grp)
	return v, srpSalt, grp, nil
}

